<h2>Pulmonary Diseases</h2><br>
<p style="font-size: 15px;text-align:justify; margin-top: 8px;">
  There is a wide range of pulmonary diseases that affect the people all around the world. Some of the common ones include:</p>
  <br>
  <h3 style="margin-top: 8px; font-weight: bold;"> Asthma</h3><br>
<p style="font-size: 15px;text-align:justify; margin-top: 8px;">
 
It is a lung disorder due to which the airways get inflamed, narrow and swollen resulting in breathing issues. Some of the common symptoms include wheezing, coughing, shortness of breath and tightness in the chest.</p>
<br>
  <h3 style="margin-top: 8px;font-weight: bold;"> Chronic Obstructive Pulmonary Disease</h3><br>
<p style="font-size: 15px;text-align:justify;margin-top: 8px;">
 

Also known as COPD, Chronic Obstructive Pulmonary Disease refers to a group of lung diseases that lead to blockage in the airflow causing breathing issues. The common conditions in COPD are chronic bronchitis and emphysema. Common signs and symptoms are wheezing, chronic cough, bluish fingernails, unexplained weight loss and shortness of breath.</p>
<br>
  <h3 style="margin-top: 8px;font-weight: bold;"> Tuberculosis
</h3><br>
<p style="font-size: 15px;text-align:justify; margin-top: 8px;margin-top: 8px;">
 

Tuberculosis is a contagious disease and can spread when the patient sneezes or coughs. Common symptoms are pain in the chest, fatigue, fever, chronic cough, shortness of breath, night sweats among others.</p>
 
 <br>
  <h3 style="margin-top: 8px;font-weight: bold;"> 
Lung Cancer
</h3>
<br>
<p style="font-size: 15px;text-align:justify;margin-top: 8px;">
 

This is the most serious lung disease during which there is an overproduction of abnormal cells in the lung that leads to the formation of a malignant tumour. The common symptoms are cough, chest pain, weight loss and wheezing.<br>
<br>
Most of the pulmonary diseases have similar symptoms. This is the reason that the person who experiences any of them must visit the best pulmonology hospital for an accurate diagnosis. After the diagnosis, the doctors can suggest the treatment options which suit the condition.</p>

