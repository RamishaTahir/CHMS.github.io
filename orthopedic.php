<h2>Spinal Fractures and Paralysis</h2><br>
<p style="font-size: 15px;">
  Spinal fracture (PARALYSIS) or spinal instability is a cause of abnormal movement between two vertebrae. This abnormality can lead to damage or pain in the nerves and spinal cord. Spinal fractures are mostly due to sports injuries, falls, car injuries and gunshots. Symptoms of spinal fracture include numbness, back or neck pain, weakness, muscle spasm, paralysis, tingling and bowel or bladder changes. Doctors conduct X-rays, CT Scan and MRI to diagnose the severity of the situation</p>
 
